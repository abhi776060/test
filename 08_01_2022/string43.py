# square and cube symbol in the area of a rectangle and volume of a cylinder
'''
input: 120.25 and 125.25
output: 120.25cm^2 and 125.25cm^3
'''

class String:
    def __init__(self):
        pass


    def symbol(self,square,cube):
        print(f'area of square is : {square}cm\u00b2')
        print(f'volume of cube is :{cube}cm\u00b3')

solution=String()
solution.symbol(120.25,125.25)